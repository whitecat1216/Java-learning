public class Data {
    //メンバ変数number
    private int number=0;
    //メンバ変数comment
    private String comment="";

    //numberのセッター
    public void setNumber(int number){
        this.number=number;
    }
    //numberのゲッター
    public int getNumber(){
        return number;
    }
    //commentのセッター
    public void setComment(String comment){
        this.comment=comment;
    }

    //commentのゲッター
    public String getComment(){
        return comment;
    }
}
